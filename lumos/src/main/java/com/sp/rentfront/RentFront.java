package com.sp.rentfront;

public class RentFront {
	private int exhibitNum;
	private int hallNum; 
	
	private String exhibitName;
	private String hallName;
	private String rentStart;
	private String rentEnd;
	
	public int getExhibitNum() {
		return exhibitNum;
	}
	public void setExhibitNum(int exhibitNum) {
		this.exhibitNum = exhibitNum;
	}
	public int getHallNum() {
		return hallNum;
	}
	public void setHallNum(int hallNum) {
		this.hallNum = hallNum;
	}
	public String getExhibitName() {
		return exhibitName;
	}
	public void setExhibitName(String exhibitName) {
		this.exhibitName = exhibitName;
	}
	public String getHallName() {
		return hallName;
	}
	public void setHallName(String hallName) {
		this.hallName = hallName;
	}
	public String getRentStart() {
		return rentStart;
	}
	public void setRentStart(String rentStart) {
		this.rentStart = rentStart;
	}
	public String getRentEnd() {
		return rentEnd;
	}
	public void setRentEnd(String rentEnd) {
		this.rentEnd = rentEnd;
	}
	
}
