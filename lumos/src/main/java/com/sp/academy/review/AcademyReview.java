package com.sp.academy.review;

public class AcademyReview {
	private int academyNum;
	private int classReviewNum;
	
	private int listNum;
	private int hitCount;
	private int boardLikeCount;
	
	private String userName;
	private String userId;
	
	private String academyName;
	private String subject;
	private String content;
	private String created;
	
	public int getAcademyNum() {
		return academyNum;
	}
	public void setAcademyNum(int academyNum) {
		this.academyNum = academyNum;
	}
	public int getClassReviewNum() {
		return classReviewNum;
	}
	public void setClassReviewNum(int classReviewNum) {
		this.classReviewNum = classReviewNum;
	}
	public int getListNum() {
		return listNum;
	}
	public void setListNum(int listNum) {
		this.listNum = listNum;
	}
	public int getHitCount() {
		return hitCount;
	}
	public void setHitCount(int hitCount) {
		this.hitCount = hitCount;
	}
	public int getBoardLikeCount() {
		return boardLikeCount;
	}
	public void setBoardLikeCount(int boardLikeCount) {
		this.boardLikeCount = boardLikeCount;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getAcademyName() {
		return academyName;
	}
	public void setAcademyName(String academyName) {
		this.academyName = academyName;
	}
	
}
